package rs.innolab.lgclientlib.restsdk;

public class Data {
	private Long balanceAfterHand;
	private long betAmount;
	private long profit;
	private int result;
	
	public Long getBalanceAfterHand() {
		return balanceAfterHand;
	}
	public void setBalanceAfterHand(Long balanceAfterHand) {
		this.balanceAfterHand = balanceAfterHand;
	}
	public long getBetAmount() {
		return betAmount;
	}
	public void setBetAmount(long betAmount) {
		this.betAmount = betAmount;
	}
	public long getProfit() {
		return profit;
	}
	public void setProfit(long profit) {
		this.profit = profit;
	}
	public int getResult() {
		return result;
	}
	public void setResult(int result) {
		this.result = result;
	}

}
